package com.teste;

public class calculadora {

    public double soma(double soma1, double soma2) {
        return soma1 + soma2;

    }

    public double subtrai(double minuendo, double subtraendo) {
        return minuendo - subtraendo;
    }

    public double multiplica(double fator1, double fator2) {
        return fator1 * fator2;
    }

    public double divisao(double dividendo, double divizor) {
        return dividendo / divizor;
    }
}

